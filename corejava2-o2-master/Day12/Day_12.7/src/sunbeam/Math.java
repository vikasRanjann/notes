package sunbeam;

public class Math {
	public int sum( int num1, int num2 ) {
		return num1 + num2;
	}
	public int sub( int num1, int num2 ) {
		return num1 - num2;
	}
	public int multiplication( int num1, int num2 ) {
		return num1 * num2;
	}
	public int division( int num1, int num2 ) {
		return num1 / num2;
	}
}
